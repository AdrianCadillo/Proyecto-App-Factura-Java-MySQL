    private void initListaProveedores(){
        setResizable(false);
        setLocationRelativeTo(null);
        botonEditar.setText("editar");botonDelete.setText("eliminar");
        botonSeleccionar.setText("Enviar");
        /************************* Name para los botones de la tabla ****************/
        botonSeleccionar.setName("botonE");
        
        /*************************  ***************************************************/
        botonEditar.setBackground(Color.yellow);botonDelete.setBackground(Color.red);
        botonSeleccionar.setBackground(Color.blue);
        modelProveedores = (DefaultTableModel) TablaProveedores.getModel();
        TablaProveedores.setModel(modelProveedores);
        PersonalizaTable();mostrarProveedores();
        TablaProveedores.setRowHeight(37);
    }
    
    private void PersonalizaTable(){
   /**
    * Tabla de lista de Proveedores
    */    
   TablaProveedores.getTableHeader().setBackground(Color.GREEN);
   TablaProveedores.getTableHeader().setForeground(Color.black);
   TablaProveedores.getTableHeader().setFont(new Font("Arial", 16, 16));
   TablaProveedores.getTableHeader().setPreferredSize(new Dimension(43, 43));
   conf.HiddenColumnTable(TablaProveedores, 8);
   conf.HiddenColumnTable(TablaProveedores, 9);
    }
    private void mostrarProveedores(){
    daoProveedor.allProveedores(modelProveedores,botonEditar,botonDelete,
    botonSeleccionar,TablaProveedores);
    }
