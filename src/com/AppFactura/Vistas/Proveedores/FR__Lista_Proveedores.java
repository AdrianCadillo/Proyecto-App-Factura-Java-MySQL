    private void TablaProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaProveedoresMouseClicked
        filaSeleccionado=TablaProveedores.getSelectedRow();
        try {
         if(conf.BotonDinamico(TablaProveedores, evt).getName().equals("botonE")){
         String RUC =TablaProveedores.getValueAt(filaSeleccionado, 0).toString();
         String RAZON_SOCIAL = TablaProveedores.getValueAt(filaSeleccionado, 1).toString();
         FR_Ingreso_Productos.txtRucProveedor.setText(RUC);
         FR_Ingreso_Productos.lblProveedorData.setText(RAZON_SOCIAL);
         dispose();
         }    
        } catch (Exception e) {
        }
    }//GEN-LAST:event_TablaProveedoresMouseClicked

